						<h3>社团标识</h3>
						<img alt="logo_dark" src="/assets/images/logo_dark.png" height="69.6" width="464.8" />
						<h3>社团概况</h3>
						<p>四川大学信息安全与网络攻防协会，简称ISDC(Information Security and Network Attack and Defense Community)，隶属于四川大学软件学院，是四川大学学术性社团。社团成立于2013年9月，第一任会长为2011级的袁珂。ISDC现有指导老师2名，2014级在编会员30名，会长1名，副会长2名。社团覆盖面为软件学院大一、大二，主要方向为信息安全产品开发、网络渗透、密码学等。</p>
						<h3>社团体系</h3>
						<p>ISDC的社团架构是开放的，不设立干事，所有资料和活动向所有人开放，但只对在编会员进行监督。这样既保证了活动的覆盖面不只限于会员，也保证了不会因为人数过多而导致活动质量下降。</p>
						<h3>活动简介</h3>
						<p>ISDC针对大一、大二开展不同方面的社团活动。针对大一，活动重心将会侧重于专业基础能力的培养，活动主要涉及C语言、Web基础培训等内容；针对大二，活动重心侧重于在已有的专业知识基础上，进行信息安全产品开发指导和网络攻防技术学习培训。活动在固定时间固定教室举行，保证了活动的质量。在活动后，我们将会通过网上训练平台进行巩固训练。除了学术型活动，我们也少不了素质类活动。组织聚餐，组织玩耍，都是我们需要带给会员们的。</p>
						<h3>对外服务</h3>
						<p>ISDC一直秉承的是开放的宗旨，我们也尽力为大家提供例如每日安全新闻摘编、信息安全比赛资料整理、安全图书馆等服务。希望能够帮助喜爱信息安全的同学，以此一道和社团发展！</p>
